     */
    public function store(Request $request)
    {
        //
        $this->validate($request, [
           'date' => 'required',
       ]);
        bookings::Create([
          'title'=>$request->type,
          'users_id'=>$request->user_id,
          'date'=>$request->date,
        ])->save();

        request()->session()->flash('message', 'booked successful');
        return redirect()->back();

